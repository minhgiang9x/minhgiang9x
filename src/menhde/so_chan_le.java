package menhde;

public class so_chan_le {
    public static  void main(String[] args){
        int num=5;
        if (num %2==0){
            System.out.println(num + "là số chẵn"); }
            else {
                System.out.println(num+" là số lẻ");
            }
        }
    }

/*
Kiểm tra số chẵn/lẻ

Nhập số nguyên, in ra "Chẵn" nếu chia hết cho 2, ngược lại in "Lẻ".

Ví dụ: Input 5 → Output "Lẻ"
 */